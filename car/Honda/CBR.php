<p style="font-size:130%; font-family:verdana;">&ensp; &ensp;  ฮอนด้า ซีบีอาร์650เอฟ มาพร้อมเครื่องยนต์แบบ 4 ลูกสูบ 4 จังหวะ DOHC 16 วาล์ว มีขนาดเครื่องยนต์ที่ให้มาขนาด 649 ซีซี มีระบบจ่ายน้ำมันเชื้อเพลิงแบบหัวฉีด (PGM-FI) มีระบบเกียร์เป็นแบบธรรมดา 6 สปีด มีระบายความร้อนด้วยน้ำ มีกำลังเครื่องยนต์เพิ่มขึ้นให้อีก 4 ตัว เป็น 89.8 แรงม้า ที่ 11,000 รอบ/นาที แรงบิด 64 นิวตันเมตร ที่ 8,000 รอบ/นาที ระบบจุระเบิดแบบ ดิจิตอล ขนาดกระบอกสูบ/ช่วงชัก 67 มม.x 46 มม. อัตราส่วนการอัดของเครื่องยนต์ 11.4:1 ความจุของถังน้ำมันเชื้อเพลิง 17.3 ลิตร ไฟหน้าที่โฉบเฉี่ยว ไฟหน้าเป็นไฟแบบ LED บังลมที่ให้มาสามารถลดแรงปะทะได้พอสมควร หน้าปัดเรือนไมล์แยกส่วนแสดงผลชัดเจนแบบดิจิตอล แฮนด์อยู่ในระดับที่พอดี ทำให้การควบคุมทำได้ง่ายดาย</p>
<p style="font-size:220%; font-family:verdana;">&ensp;  รายละเอียดข้อมูล Honda CBR650F เพิ่มเติม</p>
<p style="font-size:130%; font-family:verdana;">&ensp; &ensp; ฮอนด้า ซีบีอาร์650เอฟ มาพร้อมระบบกันสะเทือนด้านหน้าแบบ โช้คแบบเทเลสโคปิค ขนาด 41 มม. เพิ่มความนุ่มนวลในการขับขี่ ล้อหน้าเป็นแบบล้อแม็กซ์ขนาด 17 นิ้ว ยางหน้าที่ให้มามีขนาดยางอยู่ที่ 120/70-ZR17M/C ( 58W ) ระบบเบรกหน้าเป็นแบบ ดิสก์เบรคจานคู่ 320 มม. ปั้มเบรก 2 ลูกสูบ Nissin(นิชชิน) พร้อมระบบเบรกแบบ ABS ระบบกันสะเทือนด้านหลังแบบ โช้คอัพหลังเดี่ยว ปรับ Preload ได้ ช่วยในการทรงตัวของรถได้เป็นอย่างดี ล้อหลังเป็นแบบล้อแม็กซ์ขนาด 17 นิ้ว ยางหลังที่ให้มามีขนาดยางอยู่ที่ 180/55-ZR17M/C ( 73W ) ระบบเบรกหลังเป็นแบบ ดิสก์เบรค 240 มม. ปั้มเบรก 1 ลูกสูบ Nissin(นิชชิน) พร้อมระบบเบรกแบบ ABS ระยะฐานล้ออยู่ที่ 1,449 มม. ความสูงของเบาะอยู่ที่ 810 มม.</p>

