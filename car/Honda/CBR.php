<p style="font-size:250%;color:white;background-color:#8B0000; font-family:verdana;"> &ensp; CBR650F </p>
<p style="font-size:130%; font-family:verdana;">&ensp; &ensp;  Honda CBR650F เป็นบิ๊กไบค์แนวสปอร์ตฟูลแฟริ่ง ที่ได้รับความนิยมเป็นอย่างมากในตอนนี้ Honda CBR650F ได้ปรับปรุงใหม่ ไฟหน้าดูโฉบเฉี่ยวทรง V Shape แบบ Multi Reflector เป็นไฟแบบ LED ไฟท้ายก็ยังเป็นไฟแบบ LED เพิ่มความปลอดภัยในการใช้งาน หน้าปัดเรือนไมล์แยกส่วนแสดงผลชัดเจนแบบดิจิตอล ระบบกันสะเทือนหน้าและหลังยังมีสมรรถนะที่ดี ยังเพิ่มความปลอดภัยในการขับขี่ด้วยระบบเบรกแบบ ABS โฉบเฉี่ยวยิ่งกว่าเดิมด้วย ลายกราฟฟิคแบบใหม่ Honda ยังได้ใช้ระบบล็อกอัจฉริยะ H.I.S.S (Honda Lgnition Security System) กุญแจฝังชิพเพื่อป้องกันการโจรกรรม ถือว่าเป็นเทคโนโลยี่ที่ทันสมัยอย่างมาก</p>




