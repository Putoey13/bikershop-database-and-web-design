<p style="font-size:130%; font-family:verdana;">&ensp; &ensp; กลับมาสร้างตำนานอีกครั้งสำหรับ มอเตอร์ไซค์ในตำนานอย่าง Honda Rebel ที่เคยสร้างความยิ่งใหญ่ และครองตัวเองเป็นตำนานมาอย่างยาวนาน และทำให้บรรดาสาวกค่ายปีกนก เรียกร้องให้ปลุกผีขึ้นมาอีกครั้ง ซึ่ง Honda ก็ไม่ทำให้เหล่าสาวกผิดหวัง Honda ได้เปิดตัว Honda Rebel 300 และ 500 ออกสู่สายตาชาวโลกเป็นที่เรียบร้อยแล้ว ซึ่งเป็นมอเตอร์ไซค์ครุยเซอร์ สไตล์ American Custom Bobber ที่เรียกได้ว่า สวยงามเป็นอย่างยิ่ง ซึ่งเจ้า Rebel 300 และ 500 มาด้วยสโลแกนที่ว่า "Express Yourself" ซึ่งทาง Honda ได้ใช้ประเทศไทยเป็นการผลิตเจ้า Rebel 500 แล้วส่งออกไปจำหน่ายทั่วโลก ... โดยคนไทยโชคดีครับ ที่จะได้ใช้รถรุ่นนี้ก่อนใครในโลก</p>
<br>

<p style="font-size:220%; font-family:verdana;">&ensp; &ensp; รายละเอียด Honda Rebel 300</p><br>
<p style="font-size:130%; font-family:verdana;">&ensp; &ensp; รูปลักษณ์ของเจ้า Rebel ออกแบบมาได้อย่างสวยงาม ตัวเรือนไมล์มาเป็น Fully Digital แบบทรงกลมสวยงามมากๆ</p><br>

<h1 align="center"> 
<img  src="https://img.khaorot.com/newsimage/2016/12/12783866_hondas-all-new-rebel-500-bobber_ta65b2e84.jpg"width="700" height="500">
</h1>


<p style="font-size:130%; font-family:verdana;">&ensp;ตัวเครื่องยนต์ของเจ้า Rebel 300 และ 500 ใช้พื้นฐานเครื่องยนต์เดียวกับ CBR300 และ 500 โดยจะตัดลดทอนแรงม้าลงมานิดหน่อย 3-4 แรงม้า และเพิ่มแรงบิด (Torque) ขึ้นมาเพื่อความสนุกในการขับขี่ที่มากขึ้น เพราะรถแนวนี้ไม่จำเป็นต้องมีแรงม้าที่มากมาย (เอาไว้ขี่หล่อๆ ว่างั้นฮ่ะๆ) โดย Rebel 300 นั้นใช้เครื่องยนต์เดียวกับ CBR300 ซึ่งเป็นแบบสูบเดียว 286 cc หัวฉีด PGM-FI ดับเบิ้ลโอเวอร์เฮดแคมชาร์ป ระบายความร้อนด้วยน้ำ</p>

<h1 align="center"> 
<img  src="https://img.khaorot.com/newsimage/2016/12/2017-honda-rebel-300_matte-silver-rhp-1024x683.jpg"width="700" height="500">
</h1>


<p style="font-size:130%; font-family:verdana;">&ensp; &ensp; ส่วน Rebel 500 เป็นแบบสองสูบเรียง 471cc  หัวฉีด PGM-FI ดับเบิ้ลโอเวอร์เฮดแคมชาร์ป ระบายความร้อนด้วยน้ำ ที่เป็นบ๊อกเดียวกับ CBR500R ซึ่งให้กำลังแรงม้าอยู่ที่ 45 แรงม้าที่ 8500 รอบ/นาที ซึ่งใช้งานได้ครอบคลุมทั้งในเมือง และ การขี่ท่องเที่ยวทางไกล เครื่องยนต์ของสองแบบนี้ "เอาอยู่" อย่างแน่นอน</p><br>
