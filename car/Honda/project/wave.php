<p style="font-size:130%; font-family:verdana;">&ensp; &ensp;  <b>ฮอนด้า เวฟ110ไอ ปี 2019 </b>รถมอเตอร์ไซค์ครอบครัวที่ปรับเข้ากับยุคมาพร้อมไปแบบ แอลอีดี (LED) ความสว่างที่เพิ่มความสะดวกสบายให้ทุกการขับขี่จักรยานยนต์มาพร้อมกับช่องเก็บของใต้เบาะที่ใหญ่มากยิ่งขึ้นจุได้ถึง 7.4 ลิตร เครื่องยนต์ 4 จังหวะ 110 ซีซี ระบบจ่ายน้ำมันแบบหัวฉีด PGM-FI ให้ความประหยัดน้ำมันสูงถึง 63.3 กม./ลิตร พร้อมหน้าปัดเรืองไมล์ที่ดีไซน์ใหม่ออกแบบมาเพื่อผู้ใช้งานให้เห็นครบทุกฟังก์ชันการทำงานของรถได้อย่างชัดเจนยิ่งขึ้น ฮอนด้าเวฟ 110ไอใหม่นี้ สามารถเลือกสตาร์ทมือหรือสตาร์ทเท้าได้ง่ายตามการใช้งานของผู้ใช้ มีทั้งล้อแม็กและล้อซี่ลวด เปิดตัวและวางจำหน่ายทั่วประเทศตั้งแต่วันที่ 17 มกราคม 2562 นี้เป็นต้นไป</p>
<p style="font-size:130%; font-family:verdana;"> <b>&ensp; &ensp; ฮอนด้า เวฟ 110ไอ ปี 2019</b>มาพร้อมขนาดเครื่องยนต์ 109.17 ซีซี. 4 จังหวะระบบจ่ายน้ำมันเชื้อเพลิงแบบหัวฉีด PGM-FI ระบบคลัทช์เปียกแบบหลายแผ่นซ้อนกัน ระบบการติดเครื่องยนต์สตาร์ทมือระบบและสตาร์ทเท้า ความจุของถังน้ำมันอยู่ที่ 3.7 ลิตร น้ำหนักตัวรถโดยรวม 95 กิโลกรัม มั่นใจกับระบบเบรกของจักรยานยนต์ที่สามารถใช้งานง่ายปลอดภัยทุกการใช้งาน Honda Wave110i 2019 มีทั้งหมด 4 รุ่น คือ รุ่น AFS110KDFK 2TH (ล้อซี่ลวด, ดรัมเบรก, สตาร์ทเท้า) รุ่น AFS110KSFK TH (ล้อซี่ลวด, ดิสก์เบรก, สตาร์ทเท้า) รุ่น AFS110MSFK (ล้อซี่ลวด, ดิสก์เบรก, สตาร์ทมือ และสตาร์ทเท้า) รุ่น AFS110MCFK TH (ล้อแม็ก, ดิสก์เบรก, สตาร์ทมือ และสตาร์ทเท้า) ซึ่งแต่ละรุ่นมอเตอร์ไซค์ก็จะมีข้อแตกต่างกันไปตามการใช้งานและราคานั้นเอง</p>
