<p style="font-size:130%; font-family:verdana;">&ensp; &ensp; นินจา แซดเอ็กซ์-6อาร์ โฉม 2019 นี้มีหน้าปัดเรือนไมค์แบบตัว นินจา 250,นินจา 400,นินจา 650 และ นินจา 1000 ซึ่งเปิดตัวออกมาก่อนหน้านี้แล้ว หน้าปัดเรือนไมค์เป็นแบบจอ LCD ดิจิตอล มัลติฟังก์ชั่น ผสมผสานกับเข็มวัดรอบเครื่องยนต์ให้ความรู้สึกเร้าใจขับขี่สนุกมากยิ่งขึ้น ท่านั่งให้ความรู้สึกสปอร์ต ไม่ก้มมากเหมือนรุ่นพี่ zx10r ขับขี่ทางไกลสนุกแน่นอน โช๊คที่ติดตั้งมาเป็นโช๊คของ Showa แบบหัวกลับสีดำพร้อมระบบเบรกแบบ ABS ให้การขับขี่เป็นไปอย่างสนุกและปลอดภัยมากยิ่งขึ้น โครงรถจะเห็นได้ว่ามีขนาดที่ไม่ใหญ่มากนั้นหมายถึงน้ำหนักตัวรถจึงอยู่ที่ 196 กิโลกรัม ถือว่าเบามากๆสำหรับรถที่มีเครื่องยนต์ 4 สูบ ท่อเดี่ยวออกข้างสีเงินให้ความรู้สึกสปอร์ต ดูเท่ห์ ดุดัน ร้าวก้าว สะใจ ทั้งนี้ยังได้ติดตั้งเกียร์แบบ 6 สปีดระบบควิกชิพเตอร์มาให้ด้วย แต่ทว่าควิกชิพเตอร์ที่ติดตั้งมานี้จะสามารถใช้ได้แค่เพียงขึ้นเท่านั้นซึ่งไม่สามารถจะกดลงได้แต่ก็ไม่ใช่ปัญหาที่ร้ายแรงอะไรเพราะแค่ควิกชิพเตอร์ที่ให้มาก็คุ้มแล้วกับคลาส 600 ซีซี. บอกเลยว่าขับมัน ขับสนุกในแบบ Kawasaki Ninja ZX6 อย่างแน่นอน</p>
<p style="font-size:220%; font-family:verdana;"> &ensp;  <b>รายละเอียดข้อมูล Kawasaki Ninja ZX6R เพิ่มเติม</b></p>
<p style="font-size:130%; font-family:verdana;"> &ensp; &ensp;<b>คาวาซากิ แซดเอ็กซ์-6อาร์</b> โฉมใหม่นี้มาพร้อมเครื่องยนต์ 4 ลูกสูบเรียง 4 จังหวะ DOHC 4 วาล์วต่อลูกสูบ ระบบระบายความร้อนด้วยน้ำ ขนาดเครื่องยนต์อยู่ที่ 636 ซีซี. กำลังเครื่องยนต์เดิมๆสามารถทำแรงม้าสูงสุดอยู่ที่ 130 แรงม้า ที่รอบเครื่องยนต์ 13,500 รอบต่อนาที และถ้ามี Ram Air จะสามารถทำกำลังเครื่องยนต์ได้สูงสุดถึง 136 แรงม้า ที่รอบเครื่องยนต์ 13,500 รอบต่อนาที และสามารถทำแรงบิดสูงสุดอยู่ที่ 70.8 นิวตันเมตร ที่รอบเครื่องยนต์ 11,000 รอบต่อนาที มาพร้อมระบบเกียร์ธรรมดา 6 สปีดติดตั้งชุดควิกชิพเตอร์ (ขึ้นได้เพียงอย่างเดียว) มาให้เพื่อการขับขี่ที่หลากหลายและสนุกมากยิ่งขึ้น</p>
