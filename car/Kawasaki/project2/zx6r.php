<p style="font-size:200%; font-family:verdana; "> &ensp; <b>ZX6R</b> โฉมใหม่มาแล้ว โดยเมื่อคืนวันที่ 11 ตุลาคม 2561 และวันที่ 20 ตุลาคม 2561 คาวาซากิได้ทำการเปิดตัวแซดเอ็กซ์ซิกซ์อาร์ในไทย</p>
